package exercise1

import exercise1.Classes._

class ClassesSpec extends munit.FunSuite {

 test("create animal") {
   val cat = new Animal("cat", "mammal", "meat")
   assertEquals(cat.name, "cat")
   assertEquals(cat.species, "mammal")
   assertEquals(cat.food, "meat")
 }

 test("predefined animals") {
   assertEquals(Animal.parrot.name, "parrot")
   assertEquals(Animal.parrot.species, "bird")
   assertEquals(Animal.parrot.food, "vegetables")
 }

 test("eats") {
   assertEquals(Animal.goldfish.eats(clue("plants")), true)
   assertEquals(Animal.goldfish.eats("meat"), false)
 }
}
